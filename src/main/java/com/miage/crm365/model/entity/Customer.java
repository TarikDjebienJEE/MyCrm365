	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((address == null) ? 0 : address.hashCode());
		result = prime
				* result
				+ ((customerCompagnyId == null) ? 0 : customerCompagnyId
						.hashCode());
		result = prime * result
				+ ((customerId == null) ? 0 : customerId.hashCode());
		result = prime * result
				+ ((firstName == null) ? 0 : firstName.hashCode());
		result = prime * result
				+ ((homePhone == null) ? 0 : homePhone.hashCode());
		result = prime * result
				+ ((lastName == null) ? 0 : lastName.hashCode());
		result = prime * result + ((mail == null) ? 0 : mail.hashCode());
		result = prime * result
				+ ((mobilePhone == null) ? 0 : mobilePhone.hashCode());
		result = prime
				* result
				+ ((subscriptionDate == null) ? 0 : subscriptionDate.hashCode());
		result = prime * result
				+ ((workPhone == null) ? 0 : workPhone.hashCode());
		return result;
	}

	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		Customer other = (Customer) obj;
		if (address == null) {
			if (other.address != null) {
				return false;
			}
		} else if (!address.equals(other.address)) {
			return false;
		}
		if (customerCompagnyId == null) {
			if (other.customerCompagnyId != null) {
				return false;
			}
		} else if (!customerCompagnyId.equals(other.customerCompagnyId)) {
			return false;
		}
		if (customerId == null) {
			if (other.customerId != null) {
				return false;
			}
		} else if (!customerId.equals(other.customerId)) {
			return false;
		}
		if (firstName == null) {
			if (other.firstName != null) {
				return false;
			}
		} else if (!firstName.equals(other.firstName)) {
			return false;
		}
		if (homePhone == null) {
			if (other.homePhone != null) {
				return false;
			}
		} else if (!homePhone.equals(other.homePhone)) {
			return false;
		}
		if (lastName == null) {
			if (other.lastName != null) {
				return false;
			}
		} else if (!lastName.equals(other.lastName)) {
			return false;
		}
		if (mail == null) {
			if (other.mail != null) {
				return false;
			}
		} else if (!mail.equals(other.mail)) {
			return false;
		}
		if (mobilePhone == null) {
			if (other.mobilePhone != null) {
				return false;
			}
		} else if (!mobilePhone.equals(other.mobilePhone)) {
			return false;
		}
		if (subscriptionDate == null) {
			if (other.subscriptionDate != null) {
				return false;
			}
		} else if (!subscriptionDate.equals(other.subscriptionDate)) {
			return false;
		}
		if (workPhone == null) {
			if (other.workPhone != null) {
				return false;
			}
		} else if (!workPhone.equals(other.workPhone)) {
			return false;
		}
		return true;
	}

	
